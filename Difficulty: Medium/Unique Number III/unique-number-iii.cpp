class Solution {
  public:
    int getSingle(vector<int> &arr) {
        // code here
        int x1=0,x2=0;
        for(auto i:arr){
            x1=(x1^i)&~x2;
            x2=(x2^i)&~x1;
        }
        return x1;
    }
};


