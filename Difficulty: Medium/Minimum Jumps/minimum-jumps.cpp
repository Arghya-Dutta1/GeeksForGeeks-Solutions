class Solution {
  public:
    int minJumps(vector<int>& arr) {
        // code here
        int ans=0, cmx=0, lmx=0;
        for(int i=0;i<arr.size()-1;i++){
            cmx=max(cmx, i+arr[i]);
            if(lmx==i){
                if(lmx==cmx) return -1;
                ans++;
                lmx=cmx;
            }
        }
        return ans;
    }
};



